#include "libft.h"

int	ft_isalpha(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		return (1);
	}
	else if (c >= 'A' && c <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}	
}

//  The isalpha() function tests for any character for which 
// isupper(3) or islower(3) is true.  The value of the argument must 
// be representable as an unsigned char or the value of EOF.