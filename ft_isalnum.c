#include "libft.h"

int	ft_isalnum(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		return (1);
	}
	else if (c >= 'A' && c <= 'Z')
	{
		return (1);
	}
	else if (c >= '0' && c <= '9')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

//The isalnum() function tests for any character for 
// which isalpha(3) or isdigit(3) is true. 
// return 0 - false or 1 - true